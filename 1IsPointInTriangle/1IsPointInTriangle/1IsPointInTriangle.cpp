bool isPointOnLine(FVector P1, FVector P2, FVector PointToCheck)
{
	float P1P2Distance;
	float P1PtCDistance;
	float P2PtCDistance;

	P1P2Distance = sqrt((P1.X - P2.X)*(P1.X - P2.X) + (P1.Y - P2.Y)*(P1.Y - P2.Y));
	P1PtCDistance = sqrt((PointToCheck.X - P2.X)*(PointToCheck.X - P2.X) + (PointToCheck.Y - P2.Y)*(PointToCheck.Y - P2.Y));
	P2PtCDistance = sqrt((P1.X - PointToCheck.X)*(P1.X - PointToCheck.X) + (P1.Y - PointToCheck.Y)*(P1.Y - PointToCheck.Y));

	return (P1P2Distance == (P1PtCDistance + P2PtCDistance));
}

bool isPointInTriangle(FVector P1, FVector P2, FVector P3, FVector ToCheck)
{
	FVector Ort1;
	FVector Ort2;
	FVector Ort3;

	FVector V1;
	FVector V2;
	FVector V3;

	float Dot1;
	float Dot2;
	float Dot3;

	Ort1.SetCoords(-(P1.Y - P2.Y), (P1.X - P2.X));
	Ort2.SetCoords(-(P2.Y - P3.Y), (P2.X - P3.X));
	Ort3.SetCoords(-(P3.Y - P1.Y), (P3.X - P1.X));

	V1.SetCoords(ToCheck.X - P1.X, ToCheck.Y - P1.Y);
	V2.SetCoords(ToCheck.X - P2.X, ToCheck.Y - P2.Y);
	V3.SetCoords(ToCheck.X - P3.X, ToCheck.Y - P3.Y);

	Dot1 = Ort1.X*V1.X + Ort1.Y*V1.Y;
	Dot2 = Ort2.X*V2.X + Ort2.Y*V2.Y;
	Dot3 = Ort3.X*V3.X + Ort3.Y*V3.Y;

	return ((Dot1 >= 0) && (Dot2 >= 0) && (Dot3 >= 0));
}